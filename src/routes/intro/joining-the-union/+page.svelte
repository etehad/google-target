<h3>نحوه عضویت در اتحادیه</h3>

<p>
  برای عضویت در اتحادیه فقط کافیه عبارت زیر رو هر شب رأس ساعت ۲۱ در گوگل جست‌و‌جو کنید و سپس روی
  لینک این سایت کلیک کنید.
</p>

