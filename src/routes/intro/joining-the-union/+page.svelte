<p>
  این روش دقیق نیست اما بهترین روش برای شروع اتحادیه است. بعد از رشد اولیه اتحادیه و تشکیل شورای
  هماهنگی، روش های بهتری برای شمارش استفاده می‌شه.
</p>

<p>
  حتما باید این کار رو هر شب انجام بدید، فقط یک بار کافی نیست. و حتما هم باید راس ساعت ۹ شب باشه.
</p>

<p>
  برای انجام اینکار فیلترشکن لازم ندارید. فیلتر شدن خود این سایت اهمیتی نداره، چون کلیک در سایت گوگل
  اتفاق می‌افته و شمارش در سرور های گوگل انجام می‌شه. بعد از انجام جست و جو در گوگل و کلیک روی لینک
  سایت کار شما انجام شده هست و لازم نیست حتما سایت کامل باز بشه.
</p>

<p class="mt-4">
  حتما باید عبارت رو به طور دقیق وارد کنید. کلمه اضافه و کم نکنید. حرف تغییر ندید، کلا چیزی تغییر
  ندید. دقیقا همون رو وارد گوگل کنید.
</p>

