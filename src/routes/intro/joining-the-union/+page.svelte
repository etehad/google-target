<p>
  تعداد اعضای اتحادیه که در صفحه‌ی اصلی این سایت نشون داده شده ترکیبی از تعداد جست‌وجو های این عبارت
  و تعداد کلیک های انجام شده روی لینک سایت در گوگل است.
</p>

<p>
  می‌تونید نمودار نسبی حمایت از اتحادیه رو در سایت خودِ گوگل هم ببینید. دقت کنید که نمودار گوگل نسبی
  هست و فقط نشون می‌ده حمایت از اتحادیه با چه سرعتی در حال کاهش یا افزایش هست. عدد‌ها تو سایت گوگل
  درصد هستند نه تعداد کلیک یا جست‌وجو.
