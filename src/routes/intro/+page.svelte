<h3>مقدمه</h3>
<p>
  در مهر ماه ۱۴۰۱ انقلابی کاملا مردمی در ایران رخ داد. برای پیروزی باید ما مردم اتحادیه‌‌ای
  سازمان‌یافته با رهبری گروهی از متخصصین و فعالین انقلاب تشکیل دهیم. در ابتدا هدف فقط رشد اتحادیه و
  تشکیل ساختار سازماندهی است. همانند کشوری که در حال بزرگ‌ کردن و تقویت ارتش‌ خودش است ولی هنوز حمله
  نکرده. شما هم به اتحادیه بپیوندید تا با هم هدفمند، سازمان&nbsp;یافته، و متحد عمل کنیم.
</p>
