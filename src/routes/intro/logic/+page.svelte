<h3>منطق انقلاب</h3>
<h4>چرا باید انقلاب کنیم و راه دگیه ای نیست؟</h4>
<p>چون جمهوری اسلامی ترسیده و همه‌ی بودجه کشور رو خرج بقاء خودش می‌کنه تا حل مشکلات.</p>
<p>اگر متحد نشویم و انقلاب نکنیم باید انتظار اتفاقات زیر رو داشته باشیم:</p>
<ul class="pr-3">
  <li>افزایش هزینه‌ها و کاهش قدرت خرید</li>
  <li>آلوده‌تر شدن هوا</li>
  <li>محدودتر شدن اینترنت</li>
  <li>محدودتر شدن آزادی های اجتماعی، اقتصادی، سیاسی، و فرهنگی</li>
  <li>ادامه ظلم</li>
</ul>
